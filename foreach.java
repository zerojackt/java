public class foreach {
    public static void main(String[] args) {


//        * for each


        String[] group = {"tom","jenny","alice"};
        for (String e : group) {
            System.out.println(e + " is clear");
        }

        int[] numbers = {1,2,3,4};
        for (int a : numbers) {
            System.out.println(a);
        }





    }
}
