public class operator3 {
    public static void main(String[] args) {

        int b1 = (5 > 4) ? 50 : 40;

        System.out.println(b1);
//       (5 > 4) 을 연산했을때 -> true : false

        int b2 = 0;
        if (5 > 4) {
            b2 = 50; }
        else {
            b2 = 40; }
        System.out.println(b2);

        }


    }

